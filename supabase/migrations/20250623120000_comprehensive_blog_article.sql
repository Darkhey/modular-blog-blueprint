<h2 id="voraussetzungen-gebaeude">Gebäude-Voraussetzungen für optimale Effizienz</h2>

<h3 id="daemmung-optimierung">Dämmung: Die Basis für Effizienz</h3>

<p>Eine Wärmepumpe arbeitet am effizientesten bei <strong>niedrigen Vorlauftemperaturen</strong>. Ideal sind 35-45°C statt der üblichen 70°C bei Gasheizungen. Dies erfordert:</p>

<ul>
<li><strong>Gute Gebäudedämmung:</strong> U-Werte unter 0,24 W/(m²K) für Außenwände</li>
<li><strong>Moderne Fenster:</strong> Dreifachverglasung mit Uw-Werten unter 1,0 W/(m²K)</li>
<li><strong>Luftdichtigkeit:</strong> n50-Wert unter 1,5 h⁻¹</li>
</ul>

