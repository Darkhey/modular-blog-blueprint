<h2 id="arten-waermepumpen">Die verschiedenen Wärmepumpen-Arten im Detail</h2>

<h3 id="luft-wasser-waermepumpe">Luft-Wasser-Wärmepumpe: Der Allrounder</h3>

<p>Die <strong>Luft-Wasser-Wärmepumpe</strong> ist mit 85% Marktanteil die beliebteste Lösung. Sie nutzt die Außenluft als Wärmequelle und funktioniert auch bei -20°C zuverlässig. Moderne Inverter-Wärmepumpen erreichen Jahresarbeitszahlen (JAZ) von 4,0-5,5.</p>

<ul>
<li><strong>Vorteile:</strong> Geringste Installationskosten, keine Genehmigungen nötig, für fast alle Gebäude geeignet</li>
<li><strong>Nachteile:</strong> Geringere Effizienz bei sehr niedrigen Temperaturen, Geräuschentwicklung</li>
<li><strong>Kosten:</strong> 12.000-18.000€ inkl. Installation</li>
<li><strong>Ideal für:</strong> Neubauten, gut gedämmte Altbauten, Flächenheizungen</li>
</ul>

<h3 id="sole-wasser-waermepumpe">Sole-Wasser-Wärmepumpe: Maximum Effizienz</h3>

<p>Die <strong>Erdwärmepumpe</strong> nutzt die konstante Erdtemperatur von 8-12°C und erreicht dadurch die höchsten Wirkungsgrade. Mit einer JAZ von 4,5-6,0 ist sie die effizienteste Lösung.</p>

<ul>
<li><strong>Erdkollektor:</strong> Flächige Verlegung in 1,2-1,5m Tiefe, benötigt das 1,5-2fache der Wohnfläche als Grundstücksfläche</li>
<li><strong>Erdsonden:</strong> Tiefbohrung bis 100m, platzsparend aber genehmigungspflichtig</li>
<li><strong>Kosten:</strong> 20.000-30.000€ inkl. Erschließung</li>
<li><strong>Ideal für:</strong> Neubauten, große Grundstücke, langfristige Investition</li>
</ul>

<h3 id="wasser-wasser-waermepumpe">Wasser-Wasser-Wärmepumpe: Die Spitzenklasse</h3>

<p>Grundwasser als Wärmequelle bietet ganzjährig konstante 8-12°C und ermöglicht JAZ-Werte von 5,0-6,5. Diese Lösung ist jedoch nur bei geeigneten Grundwasserverhältnissen möglich.</p>

