<h3 class="text-lg font-semibold text-yellow-800 mb-2">⚠️ Wichtiger Hinweis</h3>
<p class="text-yellow-700">Auch in weniger gut gedämmten Altbauten können Wärmepumpen wirtschaftlich arbeiten - moderne Hochtemperatur-Wärmepumpen erreichen Vorlauftemperaturen bis 70°C. Eine Kombination mit Hybrid-Systemen ist oft die optimale Lösung.</p>
</div>

<h3 id="heizsystem-anpassung">Heizsystem-Optimierung</h3>

<p><strong>Flächenheizungen</strong> sind ideal für Wärmepumpen:</p>

<ul>
<li><strong>Fußbodenheizung:</strong> Vorlauftemperatur 28-35°C, höchste Effizienz</li>
<li><strong>Wandheizung:</strong> Vorlauftemperatur 30-40°C, angenehme Strahlungswärme</li>
<li><strong>Deckenheizung:</strong> Vorlauftemperatur 28-32°C, unsichtbare Installation</li>
</ul>

<p><strong>Heizkörper-Modernisierung:</strong></p>
<ul>
<li>Niedertemperatur-Heizkörper mit großen Heizflächen</li>
