<h3 id="anlagenkonzept">Anlagenkonzept und Systemintegration</h3>

<p><strong>Monovalenter Betrieb:</strong> Die Wärmepumpe deckt 100% der Heizlast ab. Ideal für gut gedämmte Gebäude mit Flächenheizung.</p>

<p><strong>Bivalenter Betrieb:</strong> Ab einer bestimmten Außentemperatur (Bivalenzpunkt) schaltet sich ein Zusatzheizer zu. Wirtschaftlich optimal bei Altbauten.</p>

<p><strong>Hybridheizung:</strong> Intelligente Kombination von Wärmepumpe und Gas-/Öl-Brennwerttherme. Das System wählt automatisch die kostengünstigste Betriebsweise.</p>

<h2 id="installation-schritt-fuer-schritt">Installation Schritt für Schritt</h2>

<h3 id="vorbereitung-genehmigungen">Vorbereitung und Genehmigungen</h3>

<p><strong>Genehmigungen je nach Wärmepumpen-Art:</strong></p>

<ul>
<li><strong>Luft-Wasser-WP:</strong> Meist genehmigungsfrei, Schallschutz beachten</li>
<li><strong>Erdwärmepumpe:</strong> Erdkollektor meist genehmigungsfrei, Erdsonden wasserrechtlich genehmigungspflichtig</li>
<li><strong>Grundwasser-WP:</strong> Wasserrechtliche Genehmigung erforderlich</li>
</ul>

<p><strong>Netzbetreiber informieren:</strong> Bei Anschlussleistungen über 12 kW muss der Netzbetreiber informiert werden. Smart-Grid-Ready-Wärmepumpen erhalten oft vergünstigte Stromtarife.</p>

<h3 id="hydraulische-einbindung">Hydraulische Einbindung</h3>

<p>Die <strong>hydraulische Integration</strong> entscheidet über Effizienz und Langlebigkeit:</p>

<ul>
<li><strong>Pufferspeicher:</strong> 50-100L pro kW Heizleistung reduziert Takten</li>
<li><strong>Hydraulische Weiche:</strong> Trennt Primär- und Sekundärkreis</li>
<li><strong>Mischventile:</strong> Ermöglichen unterschiedliche Vorlauftemperaturen</li>
<li><strong>Umwälzpumpen:</strong> Hocheffiziente Pumpen der Klasse A</li>
</ul>

