  <h2 id="arten">Dämmstoffe im Überblick</h2>
  <p>Bei der Wahl des richtigen Dämmstoffs spielen <b>Wärmedämmwert</b>, <b>Ökologie</b>, <b>Schallschutz</b> und <b>Kosten</b> eine wichtige Rolle. Lassen Sie sich am besten von einem Energieberater unterstützen. Nutzen Sie auch unseren <a href="/daemmung-isolierung" class="text-green-600 hover:underline">Dämmungsrechner</a>, um Einsparungen und Amortisation zu berechnen.</p>
  <ul>
    <li><b>Mineralwolle (Glas- oder Steinwolle):</b> Gute Dämmleistung, nicht brennbar, weit verbreitet und günstig.</li>
    <li><b>EPS (Expandiertes Polystyrol, „Styropor“):</b> Kostengünstig, gute Dämmwirkung, aber Entsorgung und Brandverhalten beachten.</li>
    <li><b>Holzfaserplatten:</b> Hervorragend schall- & sommerlicher Hitzeschutz, ökologisch vorteilhaft, etwas teurer.</li>
    <li><b>Zellulose:</b> Häufig als Einblasdämmung, sehr gute Ökobilanz, flexibel einsetzbar.</li>
    <li><b>Weitere Optionen:</b> Hanf, Schafwolle, Perlite, PUR-Hartschaum u.a. – oft Nischenanwendungen.</li>
  </ul>
  <h2 id="vergleich">Vor- und Nachteile im Vergleich</h2>
  <ul>
    <li><b>Mineralwolle:</b> Preiswert, universell, aber oft weniger „grün“ produziert.</li>
    <li><b>EPS:</b> Sehr günstig, aber problematisch bei Brandschutz und Nachhaltigkeit.</li>
    <li><b>Holzfaser:</b> Gute Speicherung von Wärme, ökologisch, sehr diffusionsoffen.</li>
    <li><b>Zellulose:</b> Gut recycelbar, oft als nachträgliche Dämmung geeignet.</li>
  </ul>
